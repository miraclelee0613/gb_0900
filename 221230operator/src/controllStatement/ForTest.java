package controllStatement;

public class ForTest {
	public static void main(String[] args) {
//		이름 10번 출력
//		10 ~ 1 출력
		for (int i = 0; i < 10; i++) {
			System.out.println(i+1+".이름");
		}
		
		System.out.println("-------------");
		
		for(int i=0; i<10; i+=1) {
			System.out.println((10 - i) + ".이름");
		}
		
		
		
		
		
		
		
		
		
		
	}
}
